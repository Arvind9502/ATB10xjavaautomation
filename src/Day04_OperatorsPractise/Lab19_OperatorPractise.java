package Day04_OperatorsPractise;
// Q4. Write a program to compare two numbers and print which one is greater, or if they are equal.
public class Lab19_OperatorPractise {
    public static void main(String[] args) {
        int num1=2;
        int num2=2;
        if (num2>num1) {
            System.out.println("num 2 is greater");
        } else if (num2==num1) {
            System.out.println("num 2 and num 1 are equal");
        }
        else {
            System.out.println("num 1 is greater");
        }

    }
}
