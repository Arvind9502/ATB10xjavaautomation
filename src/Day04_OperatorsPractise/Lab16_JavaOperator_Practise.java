package Day04_OperatorsPractise;

public class Lab16_JavaOperator_Practise {
    public static void main(String[] args) {
        //Q1. Write a program that takes two integers and prints their
        // sum, difference, product, quotient, and remainder.
        int one=4;
        int two=2;
        System.out.println("1. The sum of given numbers is:"+(one+two));
        System.out.println("2. The difference between given numbers is:"+(one-two));
        System.out.println("3. The product of given numbers is:"+(one*two));
        System.out.println("4. The quotient of given numbers is:"+(one/two));
        System.out.println("5. The remainder of given numbers is:"+(one%two));
    }
}
